package com.bmsmart.mes.mesDemo.domain.po;
/**
 * @author yjx
 *简单测试 select2 远程获取数据 
 *text --select对应的文本值
 *value--select对应的value值
 */
public class DemoTest1 {

	public DemoTest1() {
		super();
	}

	public DemoTest1(String value, String text) {
		super();
		this.value = value;
		this.text = text;
	}

	private String value;
	
	private String text;

	

	public String getValue() {
		return value;
	}

	public void setValue(String value) {
		this.value = value;
	}

	public String getText() {
		return text;
	}

	public void setText(String text) {
		this.text = text;
	}
	
	@Override
	public String toString() {
		return "DemoTest1 [value=" + value + ", text=" + text + "]";
	}
	
}
